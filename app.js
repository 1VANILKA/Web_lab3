$(document).ready(function() {
    createChart();

    $('#graphic_bar').click(function() {
        createChart('bar');
    });

    $('#graphic_line').click(function() {
        createChart('line');
    });

    $('#graphic_pie').click(function() {
        createChart('pie');
    });
});


function createChart(type='pie') {
    const basketLabels = ['logo1', 'logo2', 'logo3', 'logo4', 'logo5', 'logo6', 'logo7', 'logo8'];

    const basketData = [];

    cart = JSON.parse(localStorage.getItem('cart')) || [];
   
    basketLabels.forEach((label, index) => {
        const product = cart.find(product => product.product_id == index + 1);
        if (product) {
            basketData.push(product.quantity);
        } else {
            basketData.push(0);
        }
    });

    if (myChart) {
        myChart.destroy();
    }
    myChart = new Chart(ctx, {
        type: type,
        data: {
            labels: basketLabels,
            datasets: [{
                label: 'Your basket',
                data: basketData,
                backgroundColor: [
                    'rgba(255, 99, 132, 0.2)',
                    'rgba(54, 162, 235, 0.2)',
                    'rgba(255, 206, 86, 0.2)',
                    'rgba(75, 192, 192, 0.2)',
                    'rgba(153, 102, 255, 0.2)',
                    'rgba(255, 159, 64, 0.2)',
                    'rgba(255, 99, 132, 0.2)',
                    'rgba(54, 162, 235, 0.2)',
                ],
                borderColor: [
                    'rgba(255, 99, 132, 1)',
                    'rgba(54, 162, 235, 1)',
                    'rgba(255, 206, 86, 1)',
                    'rgba(75, 192, 192, 1)',
                    'rgba(153, 102, 255, 1)',
                    'rgba(255, 159, 64, 1)',
                    'rgba(255, 99, 132, 1)',
                    'rgba(54, 162, 235, 1)',
                ],
                borderWidth: 1
            }]
        },
        options: {
            scales: {
                y: {
                    beginAtZero: true
                }
            }
        }
    });
}
